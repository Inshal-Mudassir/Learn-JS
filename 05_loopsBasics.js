// 1. for Loop – Basic Syntax

for (initialization; condition; increment) {
  // code to run each time
}

for (let i = 1; i <= 5; i++) {
  console.log("Step", i);
}

// 2. while Loop – Basic Syntax

let i = 1;

while (i <= 5) {
  console.log(i);
  i++;
}

// 3. do...while Loop – Basic Syntax

let i = 1;

do {
  console.log(i);
  i++;
} while (i <= 5);

